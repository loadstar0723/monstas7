    const rewardAmount = Math.abs(target - entry)
    const riskReward = direction !== 'NEUTRAL' ?
      `1:${(rewardAmount / riskAmount).toFixed(1)}` : 'N/A'

    // ?úÍ∞Ñ?Ä Î∂ÑÏÑù
